Why is sexual reproduction so common when asexual reproduction is a known 
alternative? Early explanation reasoned that a wider gene pool was good for the 
species, but... (why is this wrong again?)
There must be some short-term advantage to sexual reproduction. 

The parasite hypothesis, aka the Red Queen hypothesis, for sex: 
    Parasites are selected to infect the most common genotypes. If an asexual 
    species became the majority, parasites would evolve to decimate its 
    population. This is the short-term advantage to sexual reproduction: immediate
    genetic diversity in each generation, evolving quickly just to stay slightly 
    of the parasites.
